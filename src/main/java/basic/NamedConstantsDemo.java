package basic;

public class NamedConstantsDemo {

  public static void main(String[] args) {
    final double PI;
    PI = 3.1415926;// 定义一个常量
    // 只能赋值一次

    System.out.println(PI);
    System.out.println(PI * 3);

    // PI =1.0;
    final double TUJIALIN = 0;
    System.out.println(TUJIALIN);

  }

}
